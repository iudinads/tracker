export const FOOD_PRODUCT_NAMES = Object.keys(FOOD_PRODUCTS).sort((a, b) =>
  a.localeCompare(b, "ru")
);

export function calcMacrosFromGrams(
  per100: ProductMacros,
  grams: number
): { calories: number; protein: number; fat: number; carbs: number } {
  const ratio = grams / 100;
  const round = (n: number) => Math.round(n * 10) / 10;
  return {
    calories: round(per100[0] * ratio),
    protein: round(per100[1] * ratio),
    fat: round(per100[2] * ratio),
    carbs: round(per100[3] * ratio),
  };
}

export function findProducts(query: string): string[] {
  const q = query.trim().toLowerCase();
  if (!q) return FOOD_PRODUCT_NAMES.slice(0, 12);
  return FOOD_PRODUCT_NAMES.filter((name) => name.toLowerCase().includes(q)).slice(0, 12);
}
